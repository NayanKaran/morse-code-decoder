  '.-.-.' => '"',
  '..--.' => '=',
  '-.--.-' => '-',
  '.-...' => '&',
  '---.' => '+'
}.freeze

def decode_char(morse_code)
  MORSE_CODE[morse_code]
end

def decode_word(morse_code)
  morse_code.split.map { |char| decode_char(char) }.join
end

def decode(morse_code)
  morse_code.split('  ').map { |word| decode_word(word) }.join(' ')
end

